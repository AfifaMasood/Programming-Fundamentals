 
 //calculate total annualPay of employee
#include <iostream>
using namespace std;
int main()
{
	//variable declaration
	double payAmount, payPeriod, annualPay;
	
	
	//variable initiation
	payAmount=2000;
	payPeriod = 12;
	
	
	//calculation of annual pay.
	annualPay = payAmount * payPeriod;
	cout<<endl<<endl;
	
	
	//display total annualPay on screen.
	cout<<"Total annualPay is :"<<annualPay<<endl;
	cout<<endl<<endl;
	
	//End of the programme
	return 0;
}
