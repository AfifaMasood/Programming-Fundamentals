//programm to find the circumference of  circle
#include <iostream>
using namespace std;
int main()
{
	//constant named variable
	const double PI = 3.14159;
	const double DIAMETER = 10.0;
	
	
	//variable to hold the circumference
	double circumference;
	 
	 //calculate circumferencce
	 circumference = DIAMETER * PI ;
	 
	 //display circumference
	  cout<<"The circumference of a circle is : " <<circumference<<endl;
	  return 0;
	  
	
}
